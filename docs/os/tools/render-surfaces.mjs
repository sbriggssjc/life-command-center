#!/usr/bin/env node
// Render canon blocks (docs/os/canon/blocks/*.md) into per-surface bundles (docs/os/surfaces/*.canon.md),
// and optionally into a live artifact's managed region (--write-live).
// Usage: node tools/render-surfaces.mjs [--root=<docs/os>] [--write-live]
import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from 'node:fs';
import { join, resolve } from 'node:path';

const args = process.argv.slice(2);
const ROOT = resolve((args.find(a => a.startsWith('--root=')) || '').split('=')[1] || process.cwd());
const writeLive = args.includes('--write-live');
const esc = s => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

function canonVersion() {
  const t = readFileSync(join(ROOT, 'canon', '00-INDEX.md'), 'utf8');
  const m = t.match(/CANON_VERSION[:*\s]+([0-9]+\.[0-9]+\.[0-9]+)/i);
  if (!m) throw new Error('CANON_VERSION not found in canon/00-INDEX.md');
  return m[1];
}
function loadBlocks() {
  const dir = join(ROOT, 'canon', 'blocks');
  const b = {};
  for (const f of readdirSync(dir)) if (f.endsWith('.md')) b[f.replace(/\.md$/, '')] = readFileSync(join(dir, f), 'utf8').trim();
  return b;
}
function bundle(version, id, blockIds, blocks) {
  const out = [
    `<!-- GENERATED by tools/render-surfaces.mjs — do not edit by hand. Canon: v${version} · surface: ${id} -->`,
    `<!-- Change a rule in docs/os/canon/blocks/<id>.md, bump CANON_VERSION, re-run render. -->`
  ];
  for (const bid of blockIds) {
    if (!blocks[bid]) throw new Error(`surface '${id}' needs missing block '${bid}'`);
    out.push(`\n<!-- CANON:${bid} -->\n${blocks[bid]}\n<!-- /CANON:${bid} -->`);
  }
  return out.join('\n') + '\n';
}
function upsertLive(path, body, version, begin, end) {
  let t = readFileSync(path, 'utf8');
  const region = `${begin}\n<!-- Canon: v${version} — generated; edit docs/os/canon, not here -->\n${body}\n${end}`;
  if (t.includes(begin) && t.includes(end)) t = t.replace(new RegExp(esc(begin) + '[\\s\\S]*?' + esc(end)), region);
  else t = region + '\n\n' + t;
  writeFileSync(path, t);
}

const version = canonVersion();
const manifest = JSON.parse(readFileSync(join(ROOT, 'render.manifest.json'), 'utf8'));
const blocks = loadBlocks();
const bundleDir = join(ROOT, manifest.bundleDir || 'surfaces');
if (!existsSync(bundleDir)) mkdirSync(bundleDir, { recursive: true });

for (const [id, s] of Object.entries(manifest.surfaces)) {
  const text = bundle(version, id, s.blocks, blocks);
  writeFileSync(join(bundleDir, `${id}.canon.md`), text);
  console.log(`rendered ${id}.canon.md  (v${version}, ${s.blocks.length} blocks)`);
  if (writeLive && s.liveArtifact && s.markerBegin && s.markerEnd) {
    const p = resolve(ROOT, s.liveArtifact);
    if (existsSync(p)) {
      const body = text.split('\n').filter(l => !l.startsWith('<!-- GENERATED') && !l.startsWith('<!-- Change a rule')).join('\n').trim();
      upsertLive(p, body, version, s.markerBegin, s.markerEnd);
      console.log(`  ↳ wrote managed region into ${s.liveArtifact}`);
    } else console.log(`  ↳ live artifact not found (external?): ${s.liveArtifact}`);
  }
}
console.log(`\nCanon v${version} → ${Object.keys(manifest.surfaces).length} surface bundles in ${manifest.bundleDir || 'surfaces'}/.`);
